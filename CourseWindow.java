    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
      
String sql = "INSERT INTO course (course_name, course_description, credits) VALUES (?, ?, ?)";
        try (Connection conn = DatabaseConnection.getConnection();
             PreparedStatement pstmt = conn.prepareStatement(sql)) {
            
            pstmt.setString(1, txtCourseName.getText());
            pstmt.setString(2, txtCourseDesc.getText());
            pstmt.setInt(3, Integer.parseInt(txtCredits.getText()));
            pstmt.executeUpdate();
            
            JOptionPane.showMessageDialog(this, "Course Added Successfully!");
            loadCourses(); 
            clearFields();
        } catch (SQLException | NumberFormatException ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error adding course. Check your inputs.");
        }
    }
