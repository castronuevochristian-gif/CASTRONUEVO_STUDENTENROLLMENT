private void performSearch() {
    // 1. Get the keyword from the ACTUAL search bar
    String keyword = txtSearch.getText().trim(); 
    DefaultTableModel model = (DefaultTableModel) courseTable.getModel();

    if (keyword.isEmpty()) {
        loadCourses(); 
        return;
    }

    String searchType = comboSearchType.getSelectedItem().toString();
    String columnName = "";
    
    // 2. Map the Dropdown to your DB Columns
    if (searchType.equalsIgnoreCase("Course Name")) {
        columnName = "course_name";
    } else if (searchType.equalsIgnoreCase("Units")) {
        columnName = "credits"; // Based on your MySQL screenshot
    }

    model.setRowCount(0);

    // 3. The SQL Query
    String sql = "SELECT * FROM course WHERE " + columnName + " LIKE ?";

    try (Connection conn = DatabaseConnection.getConnection();
         PreparedStatement pstmt = conn.prepareStatement(sql)) {
        
        pstmt.setString(1, "%" + keyword + "%");
        ResultSet rs = pstmt.executeQuery();

        while (rs.next()) {
            model.addRow(new Object[]{
                rs.getInt("course_id"),
                rs.getString("course_name"),
                rs.getString("course_description"),
                rs.getInt("credits")
            });
        }
    } catch (SQLException ex) {
        System.out.println("Search Error: " + ex.getMessage());
    }
}
