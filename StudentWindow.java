    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
       
        
        // shows highlighted table
        
        int selectedRow = studentTable.getSelectedRow();
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Please select a student from the table first.");
            return;
        }
        
        DefaultTableModel model = (DefaultTableModel) studentTable.getModel();
        int id = (int) model.getValueAt(selectedRow, 0);
        
        String sql = "UPDATE student SET first_name=?, last_name=?, age=?, email=? WHERE student_id=?";
        try (Connection conn = DatabaseConnection.getConnection();
             PreparedStatement pstmt = conn.prepareStatement(sql)) {
            
            pstmt.setString(1, txtFirstName.getText());
            pstmt.setString(2, txtLastName.getText());
            pstmt.setInt(3, Integer.parseInt(txtAge.getText()));
            pstmt.setString(4, txtEmail.getText());
            pstmt.setInt(5, id);
            pstmt.executeUpdate();
            
            JOptionPane.showMessageDialog(this, "Student Updated Successfully!");
            loadStudents();
            clearFields();
        } catch (SQLException | NumberFormatException ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error updating student.");
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
       int selectedRow = studentTable.getSelectedRow();
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Please select a student from the table to delete.");
            return;
        }
        
        int confirm = JOptionPane.showConfirmDialog(this, "Are you sure you want to delete this student?", "Confirm Delete", JOptionPane.YES_NO_OPTION);
        if (confirm == JOptionPane.YES_OPTION) {
            DefaultTableModel model = (DefaultTableModel) studentTable.getModel();
            int id = (int) model.getValueAt(selectedRow, 0);
            
            String sql = "DELETE FROM student WHERE student_id=?";
            try (Connection conn = DatabaseConnection.getConnection();
                 PreparedStatement pstmt = conn.prepareStatement(sql)) {
                
                pstmt.setInt(1, id);
                pstmt.executeUpdate();
                JOptionPane.showMessageDialog(this, "Student Deleted.");
                loadStudents();
                clearFields();
            } catch (SQLException ex) {
                ex.printStackTrace();
                JOptionPane.showMessageDialog(this, "Error deleting student.");
            }
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

