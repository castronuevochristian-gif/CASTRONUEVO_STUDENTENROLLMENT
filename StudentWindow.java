private void performSearch() {
    String keyword = txtSearch.getText().trim();
    DefaultTableModel model = (DefaultTableModel) studentTable.getModel();

    // 1. If the search box is empty, show everyone and STOP
    if (keyword.isEmpty()) {
        loadStudents(); 
        return;
    }

    // 2. Get the category from the Combo Box
    String searchType = comboSearchType.getSelectedItem().toString();
    String columnName = "";
    
    // 3. Map choices to your exact MySQL Column Names
    switch (searchType) {
        case "First Name": columnName = "first_name"; break;
        case "Last Name":  columnName = "last_name"; break;
        case "Age":        columnName = "age"; break;
        case "Email":      columnName = "email"; break;
        default:           columnName = "first_name";
    }

    model.setRowCount(0); // Clear table for new results

    // 4. Database Connection and Query
    String sql = "SELECT * FROM student WHERE " + columnName + " LIKE ?";

    try (Connection conn = DatabaseConnection.getConnection();
         PreparedStatement pstmt = conn.prepareStatement(sql)) {
        
        pstmt.setString(1, "%" + keyword + "%");
        ResultSet rs = pstmt.executeQuery();

        while (rs.next()) {
            model.addRow(new Object[]{
                rs.getInt("student_id"), // <-- Double check if your DB uses "id" or "student_id"
                rs.getString("first_name"),
                rs.getString("last_name"),
                rs.getInt("age"),
                rs.getString("email")
            });
        }
    } catch (SQLException ex) {
        System.out.println("Search Error: " + ex.getMessage());
    }
}
