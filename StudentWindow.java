         private void loadStudents() {
        DefaultTableModel model = (DefaultTableModel) studentTable.getModel();
        model.setRowCount(0); // Clear existing data
                
        // Ensure columns match our database
        model.setColumnIdentifiers(new String[]{"ID", "First Name", "Last Name", "Age", "Email"});
        
        String sql = "SELECT * FROM student"; //every collumn sa student(tung ID, Name, Age, Email and others)
        try (Connection conn = DatabaseConnection.getConnection();
             Statement stmt = conn.createStatement();
             ResultSet rs = stmt.executeQuery(sql)) {
            
            
            
            //Grabs data to the column names 
            while (rs.next()) {
                model.addRow(new Object[]{
                        rs.getInt("student_id"),
                        rs.getString("first_name"),
                        rs.getString("last_name"),
                        rs.getInt("age"),
                        rs.getString("email")
                });
            }
        } catch (SQLException ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error loading students from database.");
        }
    }
