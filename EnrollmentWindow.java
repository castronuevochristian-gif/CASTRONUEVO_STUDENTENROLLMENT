    private void btnEnrollActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEnrollActionPerformed
        if (comboStudent.getSelectedItem() == null || comboCourse.getSelectedItem() == null) {
            JOptionPane.showMessageDialog(this, "Please select a student and a course.");
            return;
        }
        
        // Extract the ID number from the dropdown text (e.g., "1 - John Doe" -> "1")
        String studentStr = comboStudent.getSelectedItem().toString();
        String courseStr = comboCourse.getSelectedItem().toString();
        int studentId = Integer.parseInt(studentStr.split(" - ")[0]);
        int courseId = Integer.parseInt(courseStr.split(" - ")[0]);
        
        // Get the exact current date [cite: 116]
        java.sql.Date sqlDate = new java.sql.Date(System.currentTimeMillis());

        String sql = "INSERT INTO enrolled_subject (student_id, course_id, enrollment_date) VALUES (?, ?, ?)";
        try (Connection conn = DatabaseConnection.getConnection();
             PreparedStatement pstmt = conn.prepareStatement(sql)) {
            
            pstmt.setInt(1, studentId);
            pstmt.setInt(2, courseId);
            pstmt.setDate(3, sqlDate);
            pstmt.executeUpdate();
            
            JOptionPane.showMessageDialog(this, "Student Enrolled Successfully!");
            loadEnrollments(); 
        } catch (SQLException ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error enrolling student.");
        }
    }//GEN-LAST:event_btnEnrollActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
       int selectedRow = enrollmentTable.getSelectedRow();
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Please select an enrollment from the table to delete.");
            return;
        }
        
        int confirm = JOptionPane.showConfirmDialog(this, "Are you sure you want to delete this enrollment?", "Confirm", JOptionPane.YES_NO_OPTION);
        if (confirm == JOptionPane.YES_OPTION) {
            DefaultTableModel model = (DefaultTableModel) enrollmentTable.getModel();
            int id = (int) model.getValueAt(selectedRow, 0);
            
            String sql = "DELETE FROM enrolled_subject WHERE enrollment_id=?";
            try (Connection conn = DatabaseConnection.getConnection();
                 PreparedStatement pstmt = conn.prepareStatement(sql)) {
                
                pstmt.setInt(1, id);
                pstmt.executeUpdate();
                JOptionPane.showMessageDialog(this, "Enrollment Deleted.");
                loadEnrollments();
            } catch (SQLException ex) {
                ex.printStackTrace();
            }
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

