public void loadEnrollments() {
    DefaultTableModel model = (DefaultTableModel) enrollmentTable.getModel();
    model.setRowCount(0);
    
    // SQL no longer asks for the date
    String sql = "SELECT e.enrollment_id, CONCAT(s.first_name, ' ', s.last_name), c.course_name " +
                 "FROM enrolled_subject e " +
                 "JOIN student s ON e.student_id = s.student_id " +
                 "JOIN course c ON e.course_id = c.course_id";

    try (Connection conn = DatabaseConnection.getConnection();
         Statement stmt = conn.createStatement();
         ResultSet rs = stmt.executeQuery(sql)) {
        while (rs.next()) {
            // Only adding 3 items now: ID, Student, and Course
            model.addRow(new Object[]{
                rs.getInt(1), 
                rs.getString(2), 
                rs.getString(3)
            });
        }
    } catch (Exception e) {
        System.out.println("Load Error: " + e.getMessage());
    }
}

private void performSearch() {
    String keyword = txtSearch.getText().trim();
    DefaultTableModel model = (DefaultTableModel) enrollmentTable.getModel();
    if (keyword.isEmpty()) { loadEnrollments(); return; }

    String selection = comboSearchType.getSelectedItem().toString().toLowerCase();
    String searchColumn = (selection.contains("student")) ? "CONCAT(s.first_name, ' ', s.last_name)" : 
                         (selection.contains("course")) ? "c.course_name" : "e.enrollment_id";

    model.setRowCount(0);
    // SQL no longer asks for the date here either
    String sql = "SELECT e.enrollment_id, CONCAT(s.first_name, ' ', s.last_name), c.course_name " +
                 "FROM enrolled_subject e " +
                 "JOIN student s ON e.student_id = s.student_id " +
                 "JOIN course c ON e.course_id = c.course_id " +
                 "WHERE " + searchColumn + " LIKE ?";

    try (Connection conn = DatabaseConnection.getConnection();
         PreparedStatement pstmt = conn.prepareStatement(sql)) {
        pstmt.setString(1, "%" + keyword + "%");
        ResultSet rs = pstmt.executeQuery();
        while (rs.next()) {
            model.addRow(new Object[]{
                rs.getInt(1), 
                rs.getString(2), 
                rs.getString(3)
            });
        }
    } catch (Exception e) {
        javax.swing.JOptionPane.showMessageDialog(null, "Search Error: " + e.getMessage());
    }
}
public void populateDropdowns() {
    // Clear existing items first
    comboStudent.removeAllItems();
    comboCourse.removeAllItems();

    try (Connection conn = DatabaseConnection.getConnection()) {
        // 1. Fill Students (Using first_name and last_name from your DB)
        String studentSql = "SELECT student_id, first_name, last_name FROM student";
        Statement stmtS = conn.createStatement();
        ResultSet rsS = stmtS.executeQuery(studentSql);
        while (rsS.next()) {
            // This makes the item look like "14 - Naldriel Yuan"
            String item = rsS.getInt("student_id") + " - " + rsS.getString("first_name") + " " + rsS.getString("last_name");
            comboStudent.addItem(item);
        }

        // 2. Fill Courses
        String courseSql = "SELECT course_id, course_name FROM course";
        Statement stmtC = conn.createStatement();
        ResultSet rsC = stmtC.executeQuery(courseSql);
        while (rsC.next()) {
            // This makes the item look like "6 - Physics 1"
            String item = rsC.getInt("course_id") + " - " + rsC.getString("course_name");
            comboCourse.addItem(item);
        }

    } catch (Exception e) {
        System.out.println("Dropdown Error: " + e.getMessage());
    }
}
